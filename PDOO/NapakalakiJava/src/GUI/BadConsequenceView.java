    public void setBadConsequence( BadConsequence model){
        
        BadConsequenceText.setText( model.getText() );
        
        if( model instanceof DeathBadConsequence ){
        
            Death.setText( "SÃ­" );
            Death.setVisible(true);
            DeathLabel.setVisible(true);
            LevelsLabel.setVisible(false);
            Levels.setVisible(false);
            NVisibleTreasuresLabel.setVisible(false);
            NHiddenTreasuresLabel.setVisible(false);
            NVisibleTreasures.setVisible(false);
            NHiddenTreasures.setVisible(false);
            SpecificHiddenTreasuresLabel.setVisible(false);
            SpecificHiddenTreasures.setVisible(false);
            SpecificVisibleTreasuresLabel.setVisible(false);
            SpecificVisibleTreasures.setVisible(false);

        }
        else if( model instanceof NumericBadConsequence){
            
            Death.setVisible(false);
            DeathLabel.setVisible(false);
            LevelsLabel.setVisible(true);
            Levels.setVisible(true);
            SpecificHiddenTreasuresLabel.setVisible(false);
            SpecificHiddenTreasures.setVisible(false);
            SpecificVisibleTreasuresLabel.setVisible(false);
            SpecificVisibleTreasures.setVisible(false);
            NVisibleTreasuresLabel.setVisible(true);
            NHiddenTreasuresLabel.setVisible(true);
            NVisibleTreasures.setVisible(true);
            NHiddenTreasures.setVisible(true);
            
            Levels.setText( Integer.toString( model.getLevels() ));
            NVisibleTreasures.setText( Integer.toString( ((NumericBadConsequence) model).getnVisible() ));
            NHiddenTreasures.setText( Integer.toString( ((NumericBadConsequence) model).getnHidden() ));
            
            
        }
        
        else if( model instanceof SpecificBadConsequence ){
            
            Death.setVisible(false);
            DeathLabel.setVisible(false);
            LevelsLabel.setVisible(true);
            Levels.setVisible(true);
            NVisibleTreasuresLabel.setVisible(false);
            NHiddenTreasuresLabel.setVisible(false);
            NVisibleTreasures.setVisible(false);
            NHiddenTreasures.setVisible(false);
            SpecificHiddenTreasuresLabel.setVisible(true);
            SpecificHiddenTreasures.setVisible(true);
            SpecificVisibleTreasuresLabel.setVisible(true);
            SpecificVisibleTreasures.setVisible(true);
            
            Levels.setText( Integer.toString( model.getLevels() ));
            this.fillTreasurePanel(SpecificVisibleTreasures, ((SpecificBadConsequence) model).getTvisible());
            this.fillTreasurePanel(SpecificHiddenTreasures, ((SpecificBadConsequence) model).getThidden());
            
        }
        
        repaint();
        
    }
    
  private void fillTreasurePanel (JPanel aPanel, ArrayList<TreasureKind> aList){
       
       aPanel.removeAll();
       
       for( TreasureKind t: aList){
           
           Label lab = new Label();
           lab.setText( t.toString() );
           lab.setVisible(true);
           aPanel.add(lab);
           
       }
       
       aPanel.repaint();
       aPanel.revalidate();
       
       
   }

